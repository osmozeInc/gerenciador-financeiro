<script>
    function travarNotificacoesEmGrupo(idHeader) {
        const notificacoes = document.querySelectorAll(`.notificacao[data-idHeader="${idHeader}"]`);
        notificacoes.forEach(div => {
            const label = div.querySelector('.titulo');
            const input = div.querySelector('input');

            if (label && input) {
                label.classList.add('travado');
                
                input.checked = false;
                input.disabled = true;
                input.classList.add('travado');
            }
        });
    }

    function liberarNotificacoesEmGrupo(idHeader) {
        const notificacoes = document.querySelectorAll(`.notificacao[data-idHeader="${idHeader}"]`);
        notificacoes.forEach(div => {
            const label = div.querySelector('.titulo');
            const input = div.querySelector('input');

            if (label && input) {
                label.classList.remove('travado');
                
                input.disabled = false;
                input.classList.remove('travado');
            }
        });
    }

    const idscheckbox = ['AlertasPrazosModal', 'AlertasPositivosModal', 'movimentacoesModal', 'segurancaModal'];

    idscheckbox.forEach(id => {
        const input = document.getElementById(id);
        input.addEventListener('change', function() {
            if (input.checked) {
                liberarNotificacoesEmGrupo(id);
            } else {
                travarNotificacoesEmGrupo(id);
            }
        });
    });
</script>


